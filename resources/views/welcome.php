        // Matrix characters - 1s and 0s only for The Matrix
        const matrixChars = "10";
        const fontSize = 14;
        const columns = canvas.width / fontSize;
        const trailLength = 80; // Length of each character trail

        // Initialize columns with random starting positions and trails
        const matrixColumns = [];
        for (let x = 0; x < columns; x++) {
            matrixColumns[x] = {
                y: Math.floor(Math.random() * canvas.height / fontSize),
                trail: []
            };
        }

        function drawMatrix() {
            // Clear canvas completely
            ctx.clearRect(0, 0, canvas.width, canvas.height);

            ctx.font = fontSize + "px monospace";

            // Loop through each column
            for (let i = 0; i < matrixColumns.length; i++) {
                const column = matrixColumns[i];

                // Add new character to the front of the trail
                column.trail.unshift({
                    char: matrixChars[Math.floor(Math.random() * matrixChars.length)],
                    y: column.y
                });

                // Remove old characters if trail is too long
                if (column.trail.length > trailLength) {
                    column.trail.pop();
                }

                // Draw the entire trail
                for (let j = 0; j < column.trail.length; j++) {
                    const trailChar = column.trail[j];

                    // Calculate opacity based on position in trail (front = darker, back = lighter)
                    const opacity = (trailLength - j) / trailLength * 0.9;
                    ctx.fillStyle = `rgba(180, 165, 187, ${opacity * 0.65})`;

                    // Draw character
                    ctx.fillText(trailChar.char, i * fontSize, trailChar.y * fontSize);
                }

                // Move column down
                column.y++;

                // Reset to top when off screen
                if (column.y * fontSize > canvas.height + trailLength * fontSize) {
                    column.y = -trailLength;
                    column.trail = []; // Clear trail when resetting
                }
            }
        }

        // Start the Matrix effect - medium speed
        setInterval(drawMatrix, 50);        
        
